/**
 * op_add - Function that sums two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: The sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function find the difference of two numbers
 * @a: First number
 * @b: Second Number
 *
 * Return: The differnce of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function that finds the product of two numbers
 * @a: First number
 * @b: Second Number
 *
 * Return: The product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - A function that divides two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: The division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - A function that find the modulus of two numbers
 * @a: firs number
 * @b: Second number
 *
 * Return: The modulus of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
